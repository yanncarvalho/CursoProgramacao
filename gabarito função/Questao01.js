/**
 * Questão 1. Faça quatro funções, cada uma receberá dois números e
 *  cada executará uma operação matemática: adição, subtração, multiplicação e divisão. 
 */
function sum(num1 , num2){
    return num1 + num2;
} 

function subtraction(num1 , num2){
    return num1 - num2;
} 

function multiplication(num1 , num2){
    return num1 * num2;
} 

function division(num1 , num2){
    return num1 / num2;
} 